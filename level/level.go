type Level struct {
	Tile_Map [][]int
	Enemies  []enemies.Enemy
}

func (level *Level) Update() {
	for i, enemy := range level.Enemies {
		enemy.Update()
		if enemy.GetHealth() <= 0 {
			utils.RemoveArrayElement(i, &level.Enemies)
		}
	}
}

func (level *Level) Draw(screen *ebiten.Image) {
	for y, row := range level.Tile_Map {
		for x, tile := range row {
			if tile == 1 {
				op := ebiten.DrawImageOptions{}
				op.GeoM.Translate(float64(x)*32+(camera.Camera.Pos.X), float64(y)*32+(camera.Camera.Pos.Y))

				img, _, err := ebitenutil.NewImageFromFile("./art/temp_tile.png")
				if err != nil {
					panic(err)
				}

				screen.DrawImage(img, &op)
			}
			if tile == 3 {
				op := ebiten.DrawImageOptions{}
				op.GeoM.Translate(float64(x)*32+(camera.Camera.Pos.X), float64(y)*32+(camera.Camera.Pos.Y))

				img, _, err := ebitenutil.NewImageFromFile("./art/right_ramp.png")
				if err != nil {
					panic(err)
				}

				screen.DrawImage(img, &op)
			}
			if tile == 2 {
				op := ebiten.DrawImageOptions{}
				op.GeoM.Translate(float64(x)*32+(camera.Camera.Pos.X), float64(y)*32+(camera.Camera.Pos.Y))

				img, _, err := ebitenutil.NewImageFromFile("./art/left_ramp.png")
				if err != nil {
					panic(err)
				}

				screen.DrawImage(img, &op)
			}
		}
	}

	for _, enemy := range level.Enemies {
		enemy.Draw(screen)
	}
}

func (level *Level) AddEnemy(enemy enemies.Enemy) {
	level.Enemies = append(level.Enemies, enemy)
}

func (level *Level) CheckCollision(pos utils.Vec2, size utils.Vec2) (bool, int, utils.Vec2) {
	hit := false
	hit_tile := -1
	tile_pos := utils.Vec2{}

	for y, row := range level.Tile_Map {
		for x, tile := range row {
			if tile == 1 {
				check := utils.Collide(pos, size, utils.Vec2{X: float64(x) * 32, Y: float64(y) * 32}, utils.Vec2{X: 32, Y: 31})

				if check {
					hit = true
					hit_tile = tile
					tile_pos = utils.Vec2{X: float64(x) * 32, Y: float64(y) * 32}
				}
			}
			if tile == 2 {
				check := utils.Collide(pos, size, utils.Vec2{X: float64(x) * 32, Y: float64(y) * 32}, utils.Vec2{X: 32, Y: 32})

				if check {
					hit = true
					hit_tile = tile
					tile_pos = utils.Vec2{X: float64(x) * 32, Y: float64(y) * 32}
				}
			}
			if tile == 3 {
				check := utils.Collide(pos, size, utils.Vec2{X: float64(x) * 32, Y: float64(y) * 32}, utils.Vec2{X: 32, Y: 32})

				if check {
					hit = true
					hit_tile = tile
					tile_pos = utils.Vec2{X: float64(x) * 32, Y: float64(y) * 32}
				}
			}
		}
	}

	return hit, hit_tile, tile_pos
}

